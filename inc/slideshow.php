                <p>
                    Negli anni sessanta un gruppo di sportivi romani fondò l'A.C.S.I, che venne ben presto riconosciuto dal CONI come Ente di promozione sportiva, e dal Ministero degli Interni quale Ente Nazionale con finalità  assistenziali.</p>

 
                <p>
                L'impegno nello svolgere perennemente attività nel settore della cultura, dello sport e del tempo libero ha fatto si che fossimo annoverati come una delle più importanti associazioni nazionali di promozione sociale.</p>
                
                 
                <p>
                Lo sviluppo dell'A.C.S.I. nelle varie province italiane è demandato ai Presidenti dei Comitati Provinciali, i quali, sviluppando l'attività  di indagine e di ricerca "finalizzata alla promozione ed alla diffusione dell'attività  istituzionale dell'associazione", promuovono l'organizzazione di corsi di formazione extra-scolastica della persona e l'organizzazione di numerose attività  che spaziano da quelle sportive a quelle culturali e del tempo libero, anche mediante l'incentivazione a sviluppare e costituire circoli ricreativi, culturali e società  sportive dilettantistiche, lasciando loro tutta l'autonomia giuridica ed amministrativa di cui hanno bisogno.</p>
                
                 
                <p>
                L'A.C.S.I, a norma di statuto, rappresenta e tutela nel loro complesso gli interessi dei sodalizi associati ed esercita ogni altra funzione che sia ad essa conferita da leggi, da regolamenti, da disposizioni delle competenti autorità  oppure da deliberazioni dei propri organi.

